    /**
     * Aksi yang dilakukan jika user menekan tombol Browse untuk memilih
     * lokasi dataset
     * @param evt 
     */
    private void jButton_datasetLOCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_datasetLOCActionPerformed
        //KAMUS
        int result;
        File selectedFile;
        
        //ALGORITMA
        JFileChooser fileChooser = new JFileChooser();
        
        // penambahan opsi untuk mem-filter file yang berekstensi .arff 
        fileChooser.addChoosableFileFilter(new ExtensionFilter("arff","File in ARFF format") );
        
        // inisiasi direktori awal pencarian file pada direktori home
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        
        result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            // jika user sudah memilih sebuah file
            selectedFile = fileChooser.getSelectedFile();
            jTextField_datasetLOC.setText(selectedFile.getAbsolutePath());
        }
    }//GEN-LAST:event_jButton_datasetLOCActionPerformed

