    /**
     * Aksi yang akan dilakukan jika user menekan tombol Next,
     * dimana user akan diarahkan ke halaman pembentukan model (ModelLearnerSPAM100).
     * Selain itu, akan dilakukan validasi input terhadap lokasi dataset dan file model.
     * User akan dapat mengakses ModelLearnerSPAM100 jika kedua lokasi sudah ditentukan.
     * @param evt 
     */
    private void jButton_nextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_nextActionPerformed
        //KAMUS
        String[] args = new String[2];
        
        //ALGORITMA
        
        // validasi input lokasi dataset dan model
        if (jTextField_datasetLOC.getText().equals("") || jTextField_modelLOC.getText().equals("")) {
            if (jTextField_datasetLOC.getText().equals("") && jTextField_modelLOC.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Anda belum memilih lokasi dataset dan hasil pemodelan", "Invalid Next Action", JOptionPane.WARNING_MESSAGE);
            } else if (jTextField_datasetLOC.getText().equals("") && !jTextField_modelLOC.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Anda belum memilih lokasi dataset", "Invalid Next Action", JOptionPane.WARNING_MESSAGE);
            } else if (!jTextField_datasetLOC.getText().equals("") && jTextField_modelLOC.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Anda belum memilih lokasi hasil pemodelan", "Invalid Next Action", JOptionPane.WARNING_MESSAGE);
            } 
        } else {
            // mengirimkan lokasi dataset dan hasil pemodelan kepada bagian Model Learning
            args[0] = jTextField_datasetLOC.getText();
            args[1] = jTextField_modelLOC.getText();
            ModelLearnerSPAM100.main(args);
            this.setVisible(false);
        }
        
    }//GEN-LAST:event_jButton_nextActionPerformed

